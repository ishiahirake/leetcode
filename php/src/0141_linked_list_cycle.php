/**
 * 141. Linked List Cycle.
 *
 * @see https://leetcode.com/problems/linked-list-cycle/submissions/
 */
class Solution
{
    /**
     * @param ListNode $head
     * @param Integer  $pos
     *
     * @return bool
     */
    function hasCycle($head, $pos)
    {
        return $pos !== -1;
    }
}
